/*var data type

var is the container which is use to define the varable
var is use to define global variable
*/

var a = "Prathamesh"
console.log(a);

/* let data type
let is used to define the local variable
it can only access within the block
*/

{
    let a="Pawar"
    console.log(a)
}

/*const data type
const is used to make the variable as constant which cannot be changed
if you try to change that variable then it will throw the error
*/
const b="constant name";
